          </p>
          <p>
            Estimation of emotions is an essential aspect of developing
            intelligent systems intended for crowded environments. However,
            emotion estimation in crowds remains a challenging problem due to
            the complexity in which human emotions are manifested and the
            capability of a system to perceive them in such conditions. This
            paper proposes a hierarchical Bayesian model to learn in an
            unsupervised manner the behavior of individuals and the crowd as a
            single entity and explore the relation between behavior and emotions
            to infer emotional states. Information about the movement patterns
            of individuals are described using a self-organizing map, and a
            hierarchical Bayesian network builds probabilistic models to
            identify behaviors and infer the emotional state of individuals and
            the crowd. This model is trained and tested using data produced from
            simulated scenarios that resemble real-life environments. The
            conducted experiments tested the efficiency of our method to learn,
            detect and associate behaviors with emotional states yielding
            accuracy levels of 74% for individuals and 81% for the crowd,
            similar in performance with existing methods for pedestrian behavior
            detection but with novel concepts regarding the analysis of crowds.
          </p>

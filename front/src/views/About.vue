            modeling approach that is trained on data to perceive the emotions
            of the crowd in an area under surveillance. Emotions are defined as
            evolving dynamic patterns arising due to the interaction of people
            in an environment with their relationships to the past interaction
            patterns. Camera sensors are used to track the motion of the
            individuals within a crowd scenario under observation. The data
            mining techniques are used to distinguish between different
            behaviors and events into positive and negative emotions. The
            results have been evaluated using simulated data from a proposed
            office environment.
